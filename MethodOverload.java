package com.MethodOverloading;     //MethodOverloading

	public class MethodOverload
	{
public void product(int alpha,float beta)
{
    System.out.println("Total Multiplication is: "+alpha*beta);
}  
 
public void product (int sum1,int sum2,int sum3)
{
    System.out.println("Total Multiplication is: "+sum1*sum2*sum3);
}  
}
	